export function getRelatedTours(tour, limit = 4) {
    if (!tour) return [];
    return mockTours
        .filter((item) => item.id !== tour.id && item.category === tour.category)
        .slice(0, limit);
}

export function getFirstDeparture(tour) {
    return tour?.nextDepartures?.[0];
}

